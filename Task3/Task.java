int a = 1;          // Область действия: весь метод
{
    int b = 2;      // Область действие: только внутри этих фигурных скобок
}
int c = a + b;      // Переменная b здесь не видна, так как вне скобок она не объявлялась

// правильный вариант:
int a = 1;
int b;              // Объявляем b в той же области действия, что и a
{
    b = 2;          
}
int c = a + b;  