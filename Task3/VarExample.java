public class VarExample {
    public static void main(String[] args) {
        var name = "Name";          
        var age = 25;                
        var height = 1.65;        
        var isStudent = true;       
        var letter = 'A';            
        
        System.out.println(name + " (" + age + " years)");
        System.out.println("Height: " + height);
        System.out.println("Student: " + isStudent);
        System.out.println("Grade: " + letter);
    }
}